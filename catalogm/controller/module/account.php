<?php  
class ControllerModuleAccount extends Controller {
	protected function index() {
		$this->load_language('module/account');
		
		$this->data['projectAction'] =  $this->url->link('account/product', '', 'SSL');
		
    	$this->data['logged'] = $this->customer->isLogged();
		$this->data['register'] = $this->url->link('account/register', '', 'SSL');
    	$this->data['login'] = $this->url->link('account/login', '', 'SSL');
		$this->data['logout'] = $this->url->link('account/logout', '', 'SSL');
		$this->data['forgotten'] = $this->url->link('account/forgotten', '', 'SSL');
		$this->data['account'] = $this->url->link('account/account', '', 'SSL');
		$this->data['edit'] = $this->url->link('account/edit', '', 'SSL');
		$this->data['password'] = $this->url->link('account/password', '', 'SSL');
		$this->data['wishlist'] = $this->url->link('account/wishlist');
		$this->data['order'] = $this->url->link('account/order', '', 'SSL');
		$this->data['download'] = $this->url->link('account/download', '', 'SSL');
		$this->data['return'] = $this->url->link('account/return', '', 'SSL');
		
		$this->data['newsletter'] = $this->url->link('account/newsletter', '', 'SSL');

		$this->data['address'] = $this->url->link('account/address', '', 'SSL');
		$this->data['invite'] = $this->url->link('account/invite', '', 'SSL');
		$this->data['reward'] = $this->url->link('account/reward', '', 'SSL');
		
		$this->data['coupon'] = $this->url->link('account/coupon', '', 'SSL');
		$this->data['edit'] = $this->url->link('account/edit', '', 'SSL');
		$this->data['password'] = $this->url->link('account/password', '', 'SSL');
		
		$this->data['consulation'] = $this->url->link('account/consulation', '', 'SSL');
		
		$this->load->service('payment/balance');
		// 储值信息根据用户判断
		if( $this->service_payment_balance->checkUser()) {
		    $this->data['transaction'] = $this->url->link('account/transaction', '', 'SSL');
		}
		
		if (file_exists(DIR_TEMPLATE . $this->config->get('config_template') . '/template/module/account.tpl')) {
			$this->template = $this->config->get('config_template') . '/template/module/account.tpl';
		} else {
			$this->template = 'default/template/module/account.tpl';
		}
		
		$this->render();
	}
}
?>